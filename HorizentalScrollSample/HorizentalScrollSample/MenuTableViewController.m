#pragma mark - Table view delegate

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    switch (indexPath.row) {
        case 0:
        {
            self.masterView=[[MasterViewController alloc] initWithNibName:@"MasterViewController" bundle:nil];
            [self.navigationController pushViewController:self.masterView animated:YES];
        }
            break;
        case 1:
        {
            self.tableViewObj=[[TableViewController alloc] initWithNibName:@"TableViewController" bundle:nil];
            [self.navigationController pushViewController:self.tableViewObj animated:YES];
        }
            break;   
        default:
            break;
    }
}

